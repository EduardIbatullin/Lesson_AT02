def count_vowels(text):
    """
    Функция для подсчета гласных букв в строке (русский алфавит).
    :param text: Строка, в которой нужно подсчитать гласные.
    :return: Количество гласных букв в строке.
    """
    # Список всех гласных букв русского алфавита, включая заглавные и строчные
    vowels = "аеёиоуыэюяАЕЁИОУЫЭЮЯ"

    # Перебираем каждый символ в строке и считаем количество символов, которые являются гласными
    return sum(1 for char in text if char in vowels)
