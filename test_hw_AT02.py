import pytest
from hw_AT02 import count_vowels


# Тест для строки, содержащей только гласные буквы
def test_count_vowels_only_vowels():
    # Проверяем строку из строчных гласных
    assert count_vowels("аеёио") == 5
    # Проверяем строку из заглавных гласных
    assert count_vowels("АЕЁИО") == 5


# Тест для строки, не содержащей гласных
def test_count_vowels_no_vowels():
    # Строка из согласных букв
    assert count_vowels("бгджз") == 0
    # Строка из цифр (не содержит букв)
    assert count_vowels("12345") == 0


# Тест для строки, содержащей смешанные символы (гласные, согласные, знаки препинания)
def test_count_vowels_mixed_string():
    # Проверка строки с разными буквами и символами
    assert count_vowels("Привет, мир!") == 3  # 'и', 'е', 'и'
    # Проверка строки, содержащей заглавные и строчные буквы
    assert count_vowels("Это тест!") == 3  # 'Э', 'о', 'е'
    # Проверка строки с вопросительным знаком
    assert count_vowels("Как дела?") == 3  # 'а', 'е', 'а'

# Запуск тестов можно осуществлять командой: pytest
